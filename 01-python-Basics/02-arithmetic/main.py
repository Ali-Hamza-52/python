# Arithmetic operations in Python
# Addition
a = 10
b = 20
c = a + b
print(c)

# Subtraction
a = 30
b = 20
c = a - b
print(c)


# Multiplication
a = 10
b = 20
c = a * b
print(c)


# Division
a = 10
b = 20
c = a / b
print(c)


# Modulus
a = 10
b = 20
c = a % b
print(c)


# Floor division
# Floor Division is the division that results into whole number adjusted to the left in the number line.
# It is also known as integer division.
a = 10
b = 9
c = a // b
print(c)


# Exponent
# Exponentiation is the operation of raising one number to the power of another.
# In Python, the exponentiation operator is `**`.
a = 10
b = 20
c = a ** b
print(c)